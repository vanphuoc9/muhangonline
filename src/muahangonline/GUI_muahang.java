    private void btn_luuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_luuMouseClicked
        // TODO add your handling code here:
        btn_them.setEnabled(true);
        btn_sua.setEnabled(true);

        if (isInsert == true) {
            try {
                MyConnect pt = new MyConnect();
                pt.connect();
                ResultSet rs = pt.themSP(txt_tenSP_nhap.getText(), Integer.parseInt(txt_giaSP_nhap.getText()), txt_tenloai_nhap.getText());
                pt.close();
            } catch (SQLException ex) {
                Logger.getLogger(GUI_muahang.class.getName()).log(Level.SEVERE, null, ex);
            }
            laySP();
        }
        if (isUpdate == true) {
            int row = table_sp.getSelectedRow();
            int idsp = Integer.parseInt(table_sp.getValueAt(row, 0).toString());
            //JOptionPane.showMessageDialog(this, "Sửa thành công", "Error", 1);
            try {
                MyConnect pt = new MyConnect();
                pt.connect();
                pt.capnhapSP(idsp, txt_tenSP_nhap.getText(), Integer.parseInt(txt_giaSP_nhap.getText()), txt_tenloai_nhap.getText());
                pt.close();
            } catch (SQLException ex) {
                Logger.getLogger(GUI_muahang.class.getName()).log(Level.SEVERE, null, ex);
            }
            laySP();
        }

        isInsert = false;
        isUpdate = false;
    }//GEN-LAST:event_btn_luuMouseClicked

